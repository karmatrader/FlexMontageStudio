#!/usr/bin/env python3
"""
Создание правильного Excel файла согласно EXCEL_LOGIC_DOCUMENTATION.md
"""

import pandas as pd
from pathlib import Path

def create_proper_excel():
    """Создание правильного Excel файла"""
    print("=== 📊 СОЗДАНИЕ ПРАВИЛЬНОГО EXCEL ===")
    
    # Создаем правильную структуру согласно документации
    data = [
        ["ВИДЕО 1", ""],         # Заголовок видео
        ["", "1-2"],             # Строка 2: 002.mp3 -> папка "1-2"
        ["", "1-2"],             # Строка 3: 003.mp3 -> папка "1-2"
        ["", "3-5"],             # Строка 4: 004.mp3 -> папка "3-5"
        ["", "3-5"],             # Строка 5: 005.mp3 -> папка "3-5"
        ["", "3-5"],             # Строка 6: 006.mp3 -> папка "3-5"
        ["", "6"],               # Строка 7: 007.mp3 -> папка "6"
        ["", "7"],               # Строка 8: 008.mp3 -> папка "7"
        ["", "8-10"],            # Строка 9: 009.mp3 -> папка "8-10"
        ["", "8-10"],            # Строка 10: 010.mp3 -> папка "8-10"
        ["", "8-10"],            # Строка 11: 011.mp3 -> папка "8-10"
    ]
    
    df = pd.DataFrame(data)
    
    # Сохраняем в новый файл
    output_path = Path("TestChannel/Scenario_Fixed.xlsx")
    df.to_excel(output_path, index=False, header=False)
    
    print(f"✅ Создан правильный Excel файл: {output_path}")
    print("\n📋 СОДЕРЖИМОЕ:")
    for i, row in enumerate(data):
        print(f"Строка {i+1}: A='{row[0]}' | B='{row[1]}'")
    
    print("\n🔗 СОПОСТАВЛЕНИЕ:")
    print("   Строка 2: 002.mp3 -> папка '1-2'")
    print("   Строка 3: 003.mp3 -> папка '1-2'")
    print("   Строка 4: 004.mp3 -> папка '3-5'")
    print("   Строка 5: 005.mp3 -> папка '3-5'")
    print("   Строка 6: 006.mp3 -> папка '3-5'")
    print("   Строка 7: 007.mp3 -> папка '6'")
    print("   Строка 8: 008.mp3 -> папка '7'")
    print("   Строка 9: 009.mp3 -> папка '8-10'")
    print("   Строка 10: 010.mp3 -> папка '8-10'")
    print("   Строка 11: 011.mp3 -> папка '8-10'")
    
    print("\n📊 ОЖИДАЕМЫЕ ДЛИТЕЛЬНОСТИ ПАПОК:")
    print("   Папка '1-2': 002.mp3 + 003.mp3 = ~46.4с")
    print("   Папка '3-5': 004.mp3 + 005.mp3 + 006.mp3 = ~68.4с")
    print("   Папка '6': 007.mp3 = ~23.4с")
    print("   Папка '7': 008.mp3 = ~23.7с")
    print("   Папка '8-10': 009.mp3 + 010.mp3 + 011.mp3 = ~47.4с (если 011.mp3 существует)")
    
    return output_path

if __name__ == "__main__":
    create_proper_excel()